def find_slope(x):
    x1 = x[0][0]
    y1 = x[0][1]
    x2 = x[1][0]
    y2 = x[1][1]

    if x1 == x2:
        return (((x[0]), "inf"), (x[1]))
    else:
        slope = (y2 - y1) / (x2 - x1)
        return (((x[0]), slope), (x[1]))


